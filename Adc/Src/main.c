void adc_init()
{
	RCC->AHB1ENR|=0x01;				//PortA clock enable
	RCC->APB2ENR|=0x100;			//Adc1 clock enable
	
	GPIOA->MODER|=0x0C;				//PA1 analog mode

	
	ADC1->CR1|=0x5000020;			//10 bit ADC 
	
	ADC1->CR2|=0x402;					//Continuous conversion mode	--	End of conversion selection
	
	ADC1->SMPR2|=0x38;				//ADC sample time --  SMP1 480 cycle
	
	ADC1->SQR3|=0x01;					// SQ1 2 conversions
	//adc de kaçinci in i kullaniyorsak onu açmamiz gerek
	//1-16 arasi bir deger alicak. ör: in3 -->4 conversions-->0011	
	ADC->CCR|=0x30000;				//ADC prescaler divided by 8
	
	NVIC->ISER[0]=1<<18;			//ADC interrupt enable
	
	
	
	ADC1->CR2|=0x01;					//	Enable ADC
	ADC1->CR2|=0x40000000;		//	Swstart enable(açilmaz ise adc çalismiyor)
	
	
}

